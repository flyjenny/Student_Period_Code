package absyn;

public class SetList extends Absyn {
	SetCol head;
	SetList tail;

	public SetList(SetCol h) {
		head = h;
		tail = null;
	}

	public SetList(SetCol h, SetList t) {
		head = h;
		tail = t;
	}

	public SetCol getHead() {
		return head;
	}

	public SetList getTail() {
		return tail;
	}
}
